package fastBurgerCLI;

public class Fries {

	private double friesPrice = 4.0;	
	private int friesInTray = 0;
	
	
	
	
	//Accessors and Mutators
	
	public double getFriesPrice() {
		return friesPrice;
	}

	public void setFriesPrice(double friesPrice) {
		this.friesPrice = friesPrice;
	}

	
	public int getFriesInTray() {
		return friesInTray;
	}

	public void setFriesInTray(int friesInTray) {
		this.friesInTray = friesInTray;
	}
	
}
